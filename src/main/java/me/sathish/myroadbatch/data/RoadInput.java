package me.sathish.myroadbatch.data;

public class RoadInput {
    private String objectID;
    private String roadName;
    private String surfaceType;
    private String miles;
    private String totalMiles;
    private String rusticRoadNumber;
    private String community;
    private String county;
    private String lineToWebpage;
    private String shapeLen;

    public String getObjectID() {
        return objectID;
    }

    public void setObjectID(String objectID) {
        this.objectID = objectID;
    }

    public String getRoadName() {
        return roadName;
    }

    public void setRoadName(String roadName) {
        this.roadName = roadName;
    }

    public String getSurfaceType() {
        return surfaceType;
    }

    public void setSurfaceType(String surfaceType) {
        this.surfaceType = surfaceType;
    }

    public String getMiles() {
        return miles;
    }

    public void setMiles(String miles) {
        this.miles = miles;
    }

    public String getTotalMiles() {
        return totalMiles;
    }

    public void setTotalMiles(String totalMiles) {
        this.totalMiles = totalMiles;
    }

    public String getRusticRoadNumber() {
        return rusticRoadNumber;
    }

    public void setRusticRoadNumber(String rusticRoadNumber) {
        this.rusticRoadNumber = rusticRoadNumber;
    }

    public String getCommunity() {
        return community;
    }

    public void setCommunity(String community) {
        this.community = community;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getLineToWebpage() {
        return lineToWebpage;
    }

    public void setLineToWebpage(String lineToWebpage) {
        this.lineToWebpage = lineToWebpage;
    }

    public String getShapeLen() {
        return shapeLen;
    }

    public void setShapeLen(String shapeLen) {
        this.shapeLen = shapeLen;
    }
}
